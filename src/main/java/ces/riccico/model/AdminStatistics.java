package ces.riccico.model;

public class AdminStatistics {

	private Double totalRevenue;
	
	private Integer totalAccountHost;
	
	private Integer totalHouse;
	
	private Integer totalBookingPaid;
	
	private Integer totalBookingCompleted;

	public Double getTotalRevenue() {
		return totalRevenue;
	}

	public void setTotalRevenue(Double totalRevenue) {
		this.totalRevenue = totalRevenue;
	}

	public Integer getTotalAccountHost() {
		return totalAccountHost;
	}

	public void setTotalAccountHost(Integer totalAccountHost) {
		this.totalAccountHost = totalAccountHost;
	}

	public Integer getTotalHouse() {
		return totalHouse;
	}

	public void setTotalHouse(Integer totalHouse) {
		this.totalHouse = totalHouse;
	}

	public Integer getTotalBookingPaid() {
		return totalBookingPaid;
	}

	public void setTotalBookingPaid(Integer totalBookingPaid) {
		this.totalBookingPaid = totalBookingPaid;
	}

	public Integer getTotalBookingCompleted() {
		return totalBookingCompleted;
	}

	public void setTotalBookingCompleted(Integer totalBookingCompleted) {
		this.totalBookingCompleted = totalBookingCompleted;
	}
	
	
	
}
